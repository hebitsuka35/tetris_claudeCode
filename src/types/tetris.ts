export interface Position {
  x: number;
  y: number;
}

export interface TetrisPiece {
  shape: number[][];
  position: Position;
  type: TetrominoType;
  rotation: number;
}

export type TetrominoType = 'I' | 'O' | 'T' | 'S' | 'Z' | 'J' | 'L';

export interface GameState {
  playField: number[][];
  currentPiece: TetrisPiece | null;
  nextPiece: TetrisPiece | null;
  score: number;
  level: number;
  lines: number;
  gameOver: boolean;
}

